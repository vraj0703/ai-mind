/**
 * FileStateWriter — concrete IStateWriter using TOML + JSONL files.
 */

const path = require("path");
const { IStateWriter } = require("../../domain/repositories/i_state_writer");
const { writeToml, appendJsonl } = require("../data_sources/local/toml_file");

class FileStateWriter extends IStateWriter {
  constructor(opts = {}) {
    super();
    const root = opts.projectRoot || process.cwd();
    // Post-cutover: state lives under senses/data/data_sources/local/state-live/
    // (surfaced via the state/ symlink at root).
    const stateDir = path.join(root, "senses", "data", "data_sources", "local", "state-live");
    this.sessionPath   = opts.sessionPath   || path.join(stateDir, "session.toml");
    this.heartbeatPath = opts.heartbeatPath || path.join(stateDir, "HEARTBEAT.toml");
    this.decisionsPath = opts.decisionsPath || path.join(stateDir, "decisions.jsonl");
  }

  async writeSession(state) {
    writeToml(this.sessionPath, {
      version: 2,
      session_id: state.sessionId || "",
      started_at: state.startedAt || "",
      updated_at: new Date().toISOString(),
      clean_shutdown: state.cleanShutdown || false,
      uptime_minutes: state.uptimeMinutes || 0,
      alerts: state.alerts || [],
      memo: state.memo || "",
      ...Object.fromEntries(
        (state.services || []).map(s => [`services.${s.name}`, {
          status: s.status,
          port: s.port,
          restarts: s.restarts || 0,
        }])
      ),
    }, "Raj Sadan — Session State (auto-generated by Mind v2)");
  }

  async writeHeartbeat(heartbeat) {
    writeToml(this.heartbeatPath, {
      timestamp: new Date().toISOString(),
      uptime_minutes: heartbeat.uptimeMinutes || 0,
      loop_count: heartbeat.loopCount || 0,
      services_healthy: heartbeat.servicesHealthy || 0,
      services_total: heartbeat.servicesTotal || 0,
      cpu_pct: heartbeat.cpu || 0,
      ram_pct: heartbeat.ram || 0,
      ...heartbeat.extra,
    }, "Raj Sadan — Heartbeat (auto-generated by Mind v2)");
  }

  async logDecision(decision) {
    appendJsonl(this.decisionsPath, decision.toLogEntry ? decision.toLogEntry() : decision);
  }
}

module.exports = { FileStateWriter };
